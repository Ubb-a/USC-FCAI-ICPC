    while (T--) {
        string a, b;
        cin >> a >> b;
        bool good = false, algood = false;
        
        int n = a.size();
        for (int i = 0; i <= n - 4; ++i) {
            if (a[i] == b[0] && a[i + 1] == b[1] && a[i + 2] == b[2] && a[i + 3] == b[3]) {
                good = true;
                break;
            }
        }
        
        for (int i = 0; i < n; ++i) {
            if (a[i] == b[0] && a[i + 1] == b[1] && a[i + 2] == b[2]) algood = true;
            if (a[i] == b[0] && a[i + 1] == b[1] && a[i + 2] == b[3]) algood = true;
            if (a[i] == b[0] && a[i + 1] == b[2] && a[i + 2] == b[3]) algood = true;
            if (a[i] == b[1] && a[i + 1] == b[2] && a[i + 2] == b[3]) algood = true;
            if (a[i] == b[0] && a[i + 1] == b[1] && a[i + 2] == b[2]) algood = true;
        }

        if (good) {
            cout << "good" << endl;
        } else if (algood) {
            cout << "almost good" << endl;
        } else {
            cout << "none" << endl;
        }
    }
    return 0;
