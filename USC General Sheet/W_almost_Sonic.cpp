//***********************************************************************Odd*****************************************************************************
// Function to check if a number is odd
bool isOdd(int n) {
    return n % 2 != 0;
}

//***********************************************************************prime***************************************************************************
// Function to check if a number is prime
bool isPrime(int n) {
    if (n <= 1) return false;
    for (int i = 2; i <= sqrt(n); i++) {
        if (n % i == 0) return false;
    }
    return true;
}
