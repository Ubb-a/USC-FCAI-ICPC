#include <bits/stdc++.h>
using namespace std;

//***********************************************************************Odd*****************************************************************************
// Function to check if a number is odd
bool isOdd(int n)
{
    return n % 2 != 0;
}

//***********************************************************************prime***************************************************************************
// Function to check if a number is prime
bool isPrime(int n)
{
    if (n <= 1)
        return false;
    for (int i = 2; i <= sqrt(n); i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    string l, r, x, z;
    cin >> z >> x;

    int n = z.find('|');
    l = z.substr(0, n);
    r = z.substr(n + 1);

    for (char c : x)
    {
        if (l.length() <= r.length())
        {
            l += c;
        }
        else
        {
            r += c;
        }
    }

    if (l.length() == r.length())
    {
        cout << l << "|" << r << endl;
    }
    else
    {
        cout << "Impossible" << endl;
    }

    return 0;
}