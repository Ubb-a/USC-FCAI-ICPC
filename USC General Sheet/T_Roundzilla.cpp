#include <bits/stdc++.h>
using namespace std;

//***********************************************************************Odd*****************************************************************************
// Function to check if a number is odd
bool isOdd(int n)
{
    return n % 2 != 0;
}

//***********************************************************************prime***************************************************************************
// Function to check if a number is prime
bool isPrime(int n)
{
    if (n <= 1)
        return false;
    for (int i = 2; i <= sqrt(n); i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, m;
    cin >> n >> m;
    int L[n], P[n + 1];

    for (int i = 0; i + 1 <= n; ++i)
    {
        cin >> L[i];
        P[L[i]] = i;
    }

    int z = 1;
    for (int i = 1; i < n; ++i)
    {
        if (P[i] > P[i + 1])
        {
            z++;
        }
    }

    while (m--)
    {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        int W = L[a], Y = L[b];

        swap(L[a], L[b]);
        if (P[W - 1] <= P[W] && P[W - 1] > b)
            z++;
        else if (P[W - 1] > P[W] && P[W - 1] <= b)
            z--;
        if (P[W] <= P[W + 1] && P[W + 1] < b)
            z++;
        else if (P[W] > P[W + 1] && P[W + 1] >= b)
            z--;
        P[W] = b;
        if (P[Y - 1] <= P[Y] && P[Y - 1] > a)
            z++;
        else if (P[Y - 1] > P[Y] && P[Y - 1] <= a)
            z--;
        if (P[Y] <= P[Y + 1] && P[Y + 1] < a)
            z++;
        else if (P[Y] > P[Y + 1] && P[Y + 1] >= a)
            z--;
        P[Y] = a;
        cout << z << endl;
    }
    return 0;
}