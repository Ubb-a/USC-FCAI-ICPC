#include <bits/stdc++.h>
using namespace std;

//***********************************************************************Odd*****************************************************************************
// Function to check if a number is odd
bool isOdd(int n)
{
    return n % 2 != 0;
}

//***********************************************************************prime***************************************************************************
// Function to check if a number is prime
bool isPrime(int n)
{
    if (n <= 1)
        return false;
    for (int i = 2; i <= sqrt(n); i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int z;
    cin >> z;

    char n[z][z];
    for (int x = 0; x < z; ++x)
    {
        for (int y = 0; y < z; ++y)
        {
            cin >> n[x][y];
        }
    }

    char o = n[0][0];
    char c = n[0][1];

    if (o == c)
    {
        cout << "NO" << endl;
        return 0;
    }

    bool fla = true;

    for (int x = 0; x < z; ++x)
    {
        if (n[x][x] != o || n[x][z - 1 - x] != o)
        {
            fla = false;
            break;
        }
    }

    for (int x = 0; x < z && fla; ++x)
    {
        for (int y = 0; y < z; ++y)
        {
            if (x != y && x != z - 1 - y && n[x][y] != c)
            {
                fla = false;
                break;
            }
        }
    }

    if (fla)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }
    return 0;
}