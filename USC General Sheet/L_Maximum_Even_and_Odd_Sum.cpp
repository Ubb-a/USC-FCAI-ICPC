//***********************************************************************Odd*****************************************************************************
// Function to check if a number is odd
bool isOdd(int n) {
    return n % 2 != 0;
}

//***********************************************************************prime***************************************************************************
// Function to check if a number is prime
bool isPrime(int n) {
    if (n <= 1) return false;
    for (int i = 2; i <= sqrt(n); i++) {
        if (n % i == 0) return false;
    }
    return true;
}
    signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

 ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, x = 0;
    cin >> n;

    int y = INT_MAX;
    int a[n];

    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        x += a[i];
    }

    bool z = false;
    for (int i = 0; i < n; ++i) {
        if (a[i] % 2 != 0) {
            z = true;
            if (a[i] < y) {
                y = a[i];
            }
        }
    }

    if (!z && x % 2 == 0) {
        cout << x << " " << -1 << endl;
        return 0;
    }

    if (x % 2 == 0) {
        cout << x << " " << x - y << endl;
    } else {
        cout << x - y << " " << x << endl;
    }

    return 0;
}
