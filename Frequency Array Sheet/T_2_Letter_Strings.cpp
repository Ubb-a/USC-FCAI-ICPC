#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        map<string, long long> freq;
        long long count = 0;

        for (int i = 0; i < n; i++)
        {
            string s;
            cin >> s;

            for (char c = 'a'; c <= 'k'; c++)
            {
                if (c != s[0])
                {
                    string z = s;
                    z[0] = c;
                    count += freq[z];
                }
                if (c != s[1])
                {
                    string z = s;
                    z[1] = c;
                    count += freq[z];
                }
            }

            freq[s]++;
        }

        cout << count << endl;
    }

    return 0;
}