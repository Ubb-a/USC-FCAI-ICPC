#include <bits/stdc++.h>
using namespace std;

int main()
{
 ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);

 
    string s1, s2;
    getline(cin, s1); 
    getline(cin, s2);  

    int fr[52] = {0};  

    
    for (char c : s1) {
        if (c >= 'a' && c <= 'z') {
            fr[c - 'a']++;
        } else if (c >= 'A' && c <= 'Z') {
            fr[c - 'A' + 26]++;
        }
    }

 
    bool flag = true;
    for (char c : s2) {
        if (c != ' ') {
            if (c >= 'a' && c <= 'z') {
                if (fr[c - 'a'] > 0) {
                    fr[c - 'a']--;
                } else {
                    flag = false;
                    break;
                }
            } else if (c >= 'A' && c <= 'Z') {
                if (fr[c - 'A' + 26] > 0) {
                    fr[c - 'A' + 26]--;
                } else {
                    flag = false;
                    break;
                }
            } else {
                flag = false;
                break;
            }
        }
    }

    if (flag) {
        cout << "YES" << endl;
    } else {
        cout << "NO" << endl;
    }

    return 0;

}