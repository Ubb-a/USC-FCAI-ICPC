#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, m;
    cin >> n >> m;

    int z = n + 1;

    while (true)
    {
        int isPrime = 1;
        if (z <= 1)
        {
            isPrime = 0;
        }
        else
        {
            for (int i = 2; i * i <= z; ++i)
            {
                if (z % i == 0)
                {
                    isPrime = 0;
                    break;
                }
            }
        }

        if (isPrime == 1)
        {
            break;
        }
        else
        {
            z++;
        }
    }

    if (z == m)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }
    return 0;
}