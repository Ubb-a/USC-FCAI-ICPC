#include <bits/stdc++.h>
using namespace std;

int main()
{
 ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
  int row, col;
  for (int i = 0; i < 5; ++i) {
    for (int j = 0; j < 5; ++j) {
        int v;
        cin >> v;  
        if (v == 1) { 
           row = i;  
            col = j;  
            }
        }
        
        }

    int m = abs(row - 2) + abs(col - 2);

    cout << m << endl;


    return 0;
}