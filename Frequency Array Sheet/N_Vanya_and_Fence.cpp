#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, h;
    cin >> n >> h;

    int W = 0;
    for (int i = 0; i < n; ++i)
    {
        int Z;
        cin >> Z;
        if (Z > h)
        {
            W += 2;
        }
        else
        {
            W += 1;
        }
    }

    cout << W << endl;
    return 0;
}