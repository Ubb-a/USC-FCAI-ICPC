#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n;
    cin >> n;

    int s[2 * n];
    for (int i = 0; i < 2 * n; ++i)
    {
        cin >> s[i];
    }

    int t[2 * n] = {0};
    int c = 0;
    int m = 0;

    for (int i = 0; i < 2 * n; ++i)
    {
        int x = s[i];
        if (t[x] == 1)
        {
            t[x] = 0;
            c--;
        }
        else
        {
            t[x] = 1;
            c++;
            if (c > m)
            {
                m = c;
            }
        }
    }

    cout << m << endl;

    return 0;
}