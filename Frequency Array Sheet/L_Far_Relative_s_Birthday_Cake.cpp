#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n;
    cin >> n;

    char cake[100][100];

    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < n; ++j)
        {
            cin >> cake[i][j];
        }
    }

    int Z = 0;

    for (int i = 0; i < n; ++i)
    {
        int C = 0;
        for (int j = 0; j < n; ++j)
        {
            if (cake[i][j] == 'C')
            {
                C++;
            }
        }
        Z += (C * (C - 1)) / 2;
    }

    for (int j = 0; j < n; ++j)
    {
        int C = 0;
        for (int i = 0; i < n; ++i)
        {
            if (cake[i][j] == 'C')
            {
                C++;
            }
        }
        Z += (C * (C - 1)) / 2;
    }

    cout << Z << endl;

    return 0;
}