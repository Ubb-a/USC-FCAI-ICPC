#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int t;
    cin >> t;

    string cn = "Timur";
    sort(cn.begin(), cn.end());

    while (t--)
    {
        int n;
        string s;
        cin >> n >> s;

        if (n != 5)
        {
            cout << "NO" << endl;
            continue;
        }

        string t = s;
        sort(t.begin(), t.end());

        if (t == cn)
        {
            cout << "YES" << endl;
        }
        else
        {
            cout << "NO" << endl;
        }
    }

    return 0;
}