#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        int a[n];
        int freq[101] = {0};
        for (int i = 0; i < n; ++i)
        {
            cin >> a[i];
            freq[a[i]]++;
        }

        for (int i = 0; i <= 100; ++i)
        {
            for (int j = 0; j < freq[i]; ++j)
            {
                cout << i << ' ';
            }
        }
        cout << endl;
    }

    return 0;
}
