#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;

    map<int, int> mp;
    int z = n;
    int s;

    for (int i = 0; i < n; i++)
    {
        cin >> s;
        mp[s] = 1;

        while (mp[z] == 1)
        {
            cout << z << " ";
            mp[z] = 0;
            z--;
        }
        cout << endl;
    }

    return 0;
}