#include <bits/stdc++.h>
using namespace std;

int main()
{
 ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;

    int freq[26] = {0};

    for (char c : s) {
        freq[c - 'a']++;
    }


    for (int i = 0; i < 26 && k > 0; ++i) {
        for (int j = 0; j < s.size() && k > 0; ++j) {
            if (s[j] == 'a' + i && freq[i] > 0) {
                s[j] = ' ';
                k--;
                freq[i]--; 
            }
        }
    }

    string result;
    for (char c : s) {
        if (c != ' ') {
            result.push_back(c);
        }
    }

    cout << result << endl;
    return 0;
}