#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    string g, h, p;
    cin >> g >> h >> p;

    string a = g + h;

    int freq_a[26] = {0};
    int freq_p[26] = {0};

    for (char c : a)
    {
        freq_a[c - 'A']++;
    }

    for (char c : p)
    {
        freq_p[c - 'A']++;
    }

    bool fla = true;
    for (int i = 0; i < 26; ++i)
    {
        if (freq_a[i] != freq_p[i])
        {
            fla = false;
            break;
        }
    }

    if (fla)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }
    return 0;
}