#include <bits/stdc++.h>
using namespace std;

int main()
{
    string n;
    cin >> n;

    int f[26] = {0};

    for (char c : n)
    {
        f[c - 'a'] = 1;
    }
    int count = 0;
    for (int i = 0; i < 26; i++)
    {
        if (f[i] == 1)
            count++;
    }
    if (count % 2 == 0)
    {
        cout << "CHAT WITH HER!" << endl;
    }
    else
    {
        cout << "IGNORE HIM!" << endl;
    }
}