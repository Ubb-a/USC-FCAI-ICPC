#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int tt = 1;
    // cin >> tt;
    while (tt--)
    {
        int n;
        cin >> n;
        string s;
        cin >> s;
        int freq[200] = {0};
        for (int i = 0; i < n; i++)
            freq[s[i]]++;

        for (char a = 'A'; a <= 'Z'; a++)
        {
            if (!freq[a])
                continue;
            for (char b = 'a'; b <= 'z'; b++)
            {
                if (!freq[b])
                    continue;
                freq[b]--;
                for (char c = 'a'; c <= 'z'; c++)
                {
                    if (!freq[c])
                        continue;
                    freq[c]--;
                    for (char d = 'a'; d <= 'z'; d++)
                    {
                        if (!freq[d])
                            continue;
                        cout << a << b << c << d << endl;
                    }
                    freq[c]++;
                }
                freq[b]++;
            }
        }
    }

    return 0;
}