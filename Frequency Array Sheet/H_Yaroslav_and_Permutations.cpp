#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n;
    cin >> n;

    int arr[100];
    int freq[1001] = {0};

    for (int i = 0; i < n; ++i)
    {
        cin >> arr[i];
        freq[arr[i]]++;
    }

    int maxFreq = 0;
    for (int i = 0; i <= 1000; ++i)
    {
        if (freq[i] > maxFreq)
        {
            maxFreq = freq[i];
        }
    }

    if (maxFreq > (n + 1) / 2)
    {
        cout << "NO" << endl;
    }
    else
    {
        cout << "YES" << endl;
    }
    return 0;
}