#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        string s;
        cin >> s;

        map<char, int> Z;
        int B = 0;

        for (char c : s)
        {
            if (Z[c] == 0)
            {

                B += 2;
            }
            else
            {

                B += 1;
            }
            Z[c]++;
        }

        cout << B << endl;
    }

    return 0;
}