#include <bits/stdc++.h>
using namespace std;

int main()
{
 ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
        int n;
    cin >> n;

    int a[200];
    for (int i = 0; i < n; ++i)
    {
        cin >> a[i];
    }

    int freq[200] = {0};

    for (int i = 0; i < n; ++i)
    {
        int z = 1;
        for (int j = 0; j < n; ++j)
        {
            if (a[j] > a[i])
            {
                z++;
            }
        }
        freq[i] = z;
    }

    for (int i = 0; i < n; ++i)
    {
        cout << freq[i] << " ";
    }

    return 0;
}