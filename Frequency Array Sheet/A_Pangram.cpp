#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    string s;
    cin >> n >> s;

    set<char> d;

    for (char c : s)
    {
        if (isalpha(c))
        {
            d.insert(tolower(c));
        }
    }
    if (d.size() == 26)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }

    return 0;
}