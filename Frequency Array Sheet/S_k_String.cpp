#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int x;
    cin >> x;
    string s;
    cin >> s;
    int freq[26] = {0};

    for (int i = 0; i < s.length(); i++)
    {
        freq[s[i] - 'a']++;
    }

    bool flag = true;

    for (int i = 0; i < 26; i++)
    {
        if (freq[i] % x != 0)
        {
            flag = false;
            break;
        }
    }

    if (!flag)
    {
        cout << "-1";
    }
    else
    {
        string z;
        for (int i = 0; i < 26; i++)
        {
            if (freq[i] != 0)
            {
                int count = freq[i] / x;

                for (int j = 0; j < count; j++)
                {
                    z += char(i + 'a');
                }
            }
        }

        string w;
        for (int i = 0; i < x; i++)
        {
            w += z;
        }

        cout << w << endl;
    }

    return 0;
}
