#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, p;
    cin >> p >> n;

    int freq[300] = {0};

    for (int i = 0; i < n; ++i)
    {
        int x;
        cin >> x;
        int Z = x % p;
        if (freq[Z] > 0)
        {
            cout << i + 1 << endl;

            return 0;
        }
        freq[Z]++;
    }

    cout << -1 << endl;
    return 0;
}