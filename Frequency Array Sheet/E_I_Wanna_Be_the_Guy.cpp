#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int n, p, q, level;
    cin >> n;

    int levels[101] = {0};

    cin >> p;
    for (int i = 0; i < p; ++i)
    {
        cin >> level;
        levels[level] = 1;
    }

    cin >> q;
    for (int i = 0; i < q; ++i)
    {
        cin >> level;
        levels[level] = 1;
    }

    int Z = 0;
    for (int i = 1; i <= n; ++i)
    {
        if (levels[i] == 1)
        {
            Z++;
        }
    }

    if (Z == n)
    {
        cout << "I become the guy." << endl;
    }
    else
    {
        cout << "Oh, my keyboard!" << endl;
    }
    return 0;
}